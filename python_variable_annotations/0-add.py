#!/usr/bin/env python3
"""
This module provides a type-annotated function to sum two floats.
"""


def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The sum of two floats.
    """
    return a + b
